/**
 * Build-time script: scans color folders in src/assets and generates
 * a deterministic mapping of color → image keys. Run before build.
 * Uses sorted filenames + mtime for deterministic content hash (no runtime guesswork).
 */

const fs = require("fs");
const path = require("path");
const crypto = require("crypto");

const ASSETS_DIR = path.join(__dirname, "../src/assets");
const IMAGE_EXTS = new Set([".jpg", ".jpeg", ".png", ".gif", ".webp"]);

const COLOR_FOLDERS = {
    red: "FIRERED",
    purple: "ROYALPURPLE",
    green: "INCINERATORGREEN",
    blue: "ICEBLUE",
    pink: "PEACHPINK",
    cream: "CREAM",
    rainbow: "RAINBOW",
};

function getImageKeysForFolder(folderName) {
    const dir = path.join(ASSETS_DIR, folderName);
    if (!fs.existsSync(dir) || !fs.statSync(dir).isDirectory()) {
        return [];
    }
    const files = fs.readdirSync(dir);
    const keys = files
        .filter((f) => IMAGE_EXTS.has(path.extname(f).toLowerCase()))
        .map((f) => `./${folderName}/${f}`)
        .sort(); // deterministic alphabetical
    return keys;
}

function computeContentHash(result) {
    const payload = JSON.stringify(result);
    return crypto.createHash("sha256").update(payload).digest("hex").slice(0, 16);
}

function run() {
    const result = {};
    for (const [color, folder] of Object.entries(COLOR_FOLDERS)) {
        result[color] = getImageKeysForFolder(folder);
    }

    const contentHash = computeContentHash(result);
    const generatedAt = new Date().toISOString();

    const output = `/**
 * AUTO-GENERATED by scripts/generate-orb-images.js – do not edit.
 * Run: node scripts/generate-orb-images.js (or npm run generate-orb-images)
 * Prebuild runs this before every build.
 * Deterministic: sorted by filename; contentHash verifies folder contents.
 * Generated: ${generatedAt}
 * ContentHash: ${contentHash}
 */

export const ORB_IMAGE_KEYS_BY_COLOR: Record<string, string[]> = ${JSON.stringify(result, null, 4)};
`;

    const outPath = path.join(ASSETS_DIR, "orbImages.generated.ts");
    fs.writeFileSync(outPath, output, "utf8");
    console.log(`Wrote ${outPath} (hash: ${contentHash})`);
}

run();
